print("¡¡Convertidor de números a letras!!")
print("===============================")

numeroIngresado = int(input("¿Cúal es el número que deseas convertir?: "))

if numeroIngresado == 0:
    print("El número ingresado es 'Cero'")

elif numeroIngresado == 1:
    print("El número ingresado es 'Uno'")

elif numeroIngresado == 2:
    print("El número ingresado es 'Dos'")

elif numeroIngresado == 3:
    print("El número ingresado es 'Tres'")

elif numeroIngresado == 4:
    print("El número ingresado es 'Cuatro'")

elif numeroIngresado == 5:
    print("El número ingresado es 'Cinco'")

else:
    print("El programa solo convirte hasta el número 5")

print("Fin.")
